<h1>Curiosidades sobre Edimburgo</h1>
<br>
<div class="container">
<ol class="list-unstyled"></ol>
<ol>
    <h5>Haar, la niebla de Edimburgo</h5>
    <li>Edimburgo tiene su propia niebla, llamada haar, que proviene de la costa. una bruma que se forma sobre el mar y que cubre la ciudad por varios días. Esta espesa niebla, 
    mezclada con el humo de las chimeneas de carbón daba como resultado un cielo amarillo, llamado también Pea Soup.</li>
    <br>
    <h5>Auld Reekie</h5>
    <li>En el pasado, Edimburgo era una ciudad con chimeneas humeantes que espesaban el aire y lo teñían de negro, haciendo que toda la ciudad oliera a humo.
    Aunque la ciudad tiene un peculiar olor, no es a humo, sino a la mezcla de levadura y cebada, que proviene de las fábricas de cerveza.</li>
    <br>
    <h5>Puedes encontrar al menos de 5 restaurantes con estrellas Michelín</h5>
    <li>No es tan fácil encontrar restaurantes que estén premiados con las prestigiosas estrellas Micheliín en Escocia, por ello nombraremos los 5 que pueden encontrar en Edimburgo:
    “Number One” en Princes Street, “21212” en Royal Terrace and Castle Terrace, “Restaurant Martin Wishart” en The Shore y “The Kitchin” en Commercial Quay.</li>
    <br>
    <h5>El extraño corazón del Midlothian</h5>
    <li>Junto a la Catedral de San Giles, en la Royal Mile, los visitantes pueden ver un corazón hecho con losas de granito sobre el pavimento. Es el lugar en el que se encontraba la antigua prisión de Edimburgo,
     construida durante el siglo XV y demolida en 1817. Hoy en día es costumbre escupir en el interior del corazón para tener buena suerte, por lo que no es buena idea pasar por encima de él porque podríamos resbalar, aunque ésa no es la única razón por la que no es conveniente pisar el corazón: cuenta la leyenda que quien lo pisa, no tiene nunca suerte en el amor.</li>
    <br>
    <h5>El castillo de Edimburgo</h5>
    <li>El Castillo de Edimburgo, en Castle Rock, es el monumento más importante y conocido de la ciudad. 
    Está edificado sobre un volcán extinto y desde la última vez que entró en erupción han pasado más de 350 millones de años.</li>
        <br>
        <h5>Piedra del destino</h5>
    <li>En el Castillo de Edimburgo se guarda la «Piedra del Destino». Es una piedra que tradicionalmente está presente en la coronación de la realeza escocesa e inglesa. Aunque existe una leyenda que dice que la actual Piedra del Destino no es la original, 
    sino que fue cambiada por una falsificación en algún momento de la historia.</li>
        <br>
        <h5>Edimburgh Fringe Festival</h5>
        <li>Es el festival artístico más grande del mundo. En principio fue un compendio de actos marginales del Festival Internacional de Edimburgo que tenía lugar durante el mes de agosto. 
    Puedes ver cerca de 3.000 espectáculos en 300 escenarios y más de 50.000 representaciones. Acude gente de todo el mundo.</li>
    <br>
    <h5>Ciudad de la literatura</h5>
    <li>Edimburgo fue la primera ciudad del mundo en ser nombrada por la UNESCO «Ciudad de la Literatura». Este ocurrió por su herencia y prestigio literario ya que autores de la talla de Conan Doyle, Robert Louis Stevenson, Muriel Spark, 
    Irvine Welsh o Sir Walter Scott nacieron en ella. Eso sin contar que JK Rowling escribió el primer libro de la saga de Harry Potter en una cafetería de la ciudad.</li>
    <br>
    <h5>Haggis, el plato típico</h5>
    <li>Un plato que elaboraban las familias más pobres con las partes de los animales que nadie quería: hígado, corazón, pulmones… 
    todo ello embutido, condimentado y cocido durante varias horas.</li>
    <br>
    <h5>El cañón de la una</h5>
    <li>En el Castillo de Edimburgo, todos los días, a la una del mediodía (excepto los domingos y otras festividades), tiene lugar una tradición que se lleva a cabo desde 1861. A la una, el General de Artillería dispara un moderno cañón, 
    que perpetúa la tradición, pero que funciona sobre todo como reclamo turístico.</li>
    </ol>
